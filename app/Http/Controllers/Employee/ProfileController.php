     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $profile = Profile::find($id);
        $profile->first_name = $request->first_name;
        $profile->last_name = $request->last_name;
        $profile->telephone = $request->telephone ? $request->telephone : "";
        $profile->github_account = $request->github_account ?$request->github_account : "";
        $profile->save();

        $user = $profile->user()->first();
        $user->email = $request->email;
        $user->save();

        return redirect()->route('profile.index',compact('profile'))->with('status','Profile edited successfully.');
    }

