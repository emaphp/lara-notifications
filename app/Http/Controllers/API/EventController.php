    public function getEvent($slug)
    {
        $event = Event::where('slug',$slug)->first();
        if (!is_null($event)) {
            $users = $event->users()->get();
            $event['guests'] = $users->values()->all();
        }
        return response()->json([
            'event' => $event
        ]);
    }
